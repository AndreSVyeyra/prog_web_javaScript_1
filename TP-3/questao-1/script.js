
// Operador de igualdade simples (==) : Compara dois valores  e retorna true se eles forem iguais idependente do tipo. 
// Operador de diferença simples (!=) :  Compara dois valores e retorna true se eles forem diferentes
// Operador de estrita (===): Compara dois valores e retorna true se forem iguais de do mesmo tipo


// Exemplo operador de igualdade
console.log(5 == '5');

// Exemplo operador de igualdade estrista
console.log(5 === '5');

// Exemplo operador de igualdade diferença simples 
console.log(5 !== '5');

